class Subtraction {
    public static void main(String[] args) {
        int ai = 10;
        int bi = 13;
        System.out.println("int: " + (ai - bi));

        byte au = 10;
        byte bu = 2;
        System.out.println("byte: " + (au - bu));

        short cs = 11;
        short ds = 4;
        System.out.println("short: " + (cs - ds));

        long l = 1376481509L;
        long m = 1248974L;
        System.out.println("long: " + (l - m));

        float f = 12.23f;
        float g = 12.372f;
        System.out.println("float: " + (f - g));

        double du = 1234.7867134d;
        double fu = 1234.734d;
        System.out.println("double : " + (du - fu));

        String x = "atharv ";
        String y = "Badekar";
        System.out.println("String : " + (x + y));

        char o = 'a';
        char p = 'b';
        System.out.println("Char : " + (o + p));
    }
}
